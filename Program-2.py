num2 = int(input())
# Addition
print("Your Number", num1, "And Number", num2, "Addition Is :", num1 + num2)
# Division
print("Your Number", num1, "And Number", num2, "Division Is :", num1 / num2)
# Multiplication
print("Your Number", num1, "And Number", num2, "Multiplication Is :", num1 * num2)
# Subtraction
print("Your Number", num1, "And Number", num2, "Subtraction Is :", num1 - num2)



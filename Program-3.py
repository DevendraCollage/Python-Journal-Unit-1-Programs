# Definition : WAP TO CONCATENATE TWO STRING AND PRINT IT.

# Get the name of the user
print("Enter Your Name : ", end="")
name = input()
# Get the surname of the user
print("Enter Your Surname : ", end="")
surname = input()
# Concatenate the username or surname
fullname = name + surname
print("Your Full Name Is : ", end="")
print(fullname)


# Output :
"""
Enter Your Name : Devendra
Enter Your Surname : Parmar
Your Full Name Is : Devendra Parmar
"""